args<-commandArgs(TRUE)
options(digits=22)
library("Matrix")
library("matrixStats")
library("einsum")


A = matrix(seq(1,300), 5, 60, byrow=TRUE)/1000;
B = matrix(seq(1,150), 5, 30, byrow=TRUE)/1000;
C = matrix(seq(1,500), 5, 100, byrow=TRUE)/1000;
D = matrix(seq(1,1800), 60, 30, byrow=TRUE)/1000;
E = matrix(seq(1,6000), 100, 60, byrow=TRUE)/1000;
F = matrix(seq(1,3000), 100, 30, byrow=TRUE)/1000;

R = einsum("fx,fg,fz,xg,zx,zg->g",A,B,C,D,E,F)

writeMM(as(R, "CsparseMatrix"), paste(args[2], "S", sep="")); 
